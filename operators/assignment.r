# Operator	Name	Example
# +	Addition	x + y	
# -	Subtraction	x - y	
# *	Multiplication	x * y	
# /	Division	x / y	
# ^	Exponent	x ^ y
# %%	Modulus (Remainder from division)	x %% y	
# %/%	Integer Division	x %/% y

n1 <- 4 
6 -> n2

paste("Addition: ", n1 + n2)
paste("Subtraction: ",n1 - n2)
paste("Multiplication: ",n1 * n2)
paste("Division: ",n1 / n2)
paste("Exponent: ",n1 ^ n2)
paste("Modulus: ",n1 %% n2)
paste("Integer Division: ",n1 %/% n2)